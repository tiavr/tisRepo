package sentinelleGrippe;


public class Incidence {
    private int date, valeur;

    public Incidence(int date, int valeur) {
        this.date = date;
        this.valeur = valeur;
    }

    public int getDate() {
        return date;
    }

    public int getValeur() {
        return valeur;
    }
    
    
}
